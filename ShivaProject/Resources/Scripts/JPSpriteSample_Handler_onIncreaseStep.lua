function JPSpriteSample.onIncreaseStep (  )
--------------------------------------------------------------------------------
	
    if ( not this.bMoreDown ( ) )
    then
        return
    end
    
    this.increaseStep ( )
    if ( this.bMoreDown ( ) )
    then
        this.postEvent ( 0.1, "onIncreaseStep" )
    end
	
--------------------------------------------------------------------------------
end
