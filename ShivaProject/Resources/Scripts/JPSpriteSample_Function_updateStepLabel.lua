function JPSpriteSample.updateStepLabel ( )
--------------------------------------------------------------------------------
	
    local hLabel = hud.getComponent ( this.getUser ( ), "JPSpriteSample.StepLabel" )
    hud.setLabelText ( hLabel, string.format ( "%i", this.nStep ( ) ) )
	
--------------------------------------------------------------------------------
end
