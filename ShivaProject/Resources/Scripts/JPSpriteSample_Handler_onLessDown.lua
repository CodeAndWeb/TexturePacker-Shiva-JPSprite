function JPSpriteSample.onLessDown ( bDown )
--------------------------------------------------------------------------------
	
    this.bLessDown ( bDown )
    if ( bDown )
    then
        this.decreaseStep ( )
        this.postEvent ( 0.5, "onDecreaseStep" )
    end
	
--------------------------------------------------------------------------------
end
