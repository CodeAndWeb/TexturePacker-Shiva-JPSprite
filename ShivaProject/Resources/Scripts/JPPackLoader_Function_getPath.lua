function JPPackLoader.getPath ( )
--------------------------------------------------------------------------------
	
    local sPath = this.sLocalPath ( )
    if ( string.isEmpty ( sPath ) )
    then
        sPath = "file://" .. application.getPackDirectory ( )
    end
    
    return sPath
    
--------------------------------------------------------------------------------
end
