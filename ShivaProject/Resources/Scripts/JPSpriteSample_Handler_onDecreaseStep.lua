function JPSpriteSample.onDecreaseStep (  )
--------------------------------------------------------------------------------
	
    if ( not this.bLessDown ( ) )
    then
        return
    end
    
    this.decreaseStep ( )
    if ( this.bLessDown ( ) )
    then
        this.postEvent ( 0.1, "onDecreaseStep" )
    end
	
--------------------------------------------------------------------------------
end
