function JPSpriteSample.onMoreDown ( bDown )
--------------------------------------------------------------------------------
	
    this.bMoreDown ( bDown )
    if ( bDown )
    then
        this.increaseStep ( )
        this.postEvent ( 0.5, "onIncreaseStep" )
    end
	
--------------------------------------------------------------------------------
end
