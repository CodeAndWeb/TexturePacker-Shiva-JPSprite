function JPSpriteData.onWalk (  )
--------------------------------------------------------------------------------
	
    this.bWalking ( true )
	
    local hSprite = this.getSprite ( )
    local bFlipX = this.nDirection ( ) < 0
    JPSprite.setComponentAnimation ( hSprite, "walk", true, bFlipX, nil, true )
	
--------------------------------------------------------------------------------
end
